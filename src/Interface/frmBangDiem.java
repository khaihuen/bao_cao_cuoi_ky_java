    private void loadCombos() {
        try {
            Connection conn = new Connect().connectSQL();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT MaSV FROM SinhVien");
            while (rs.next()) {
                cbSV.addItem(rs.getString(1));
            }
            rs = st.executeQuery("SELECT MaMon FROM MonHoc");
            while (rs.next()) {
                cbMon.addItem(rs.getString(1));
            }
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi load combo: " + e.getMessage());
        }
    }

    private void loadData() {
        try {
            model.setRowCount(0);
            Connection conn = new Connect().connectSQL();
            ResultSet rs = conn.createStatement().executeQuery("SELECT * FROM BangDiem");
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("MaSV"),
                    rs.getString("MaMon"),
                    rs.getFloat("Diem")
                });
            }
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi tải dữ liệu: " + e.getMessage());
        }
    }
