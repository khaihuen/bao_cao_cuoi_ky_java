    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            Connection conn = new Connect().connectSQL();
            PreparedStatement ps = conn.prepareStatement("INSERT INTO BangDiem VALUES(?,?,?)");
            ps.setString(1, cbSV.getSelectedItem().toString());
            ps.setString(2, cbMon.getSelectedItem().toString());
            ps.setFloat(3, Float.parseFloat(txtDiem.getText()));
            ps.executeUpdate();
            conn.close();
            loadData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi thêm: " + e.getMessage());
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        try {
            Connection conn = new Connect().connectSQL();
            PreparedStatement ps = conn.prepareStatement("UPDATE BangDiem SET Diem=? WHERE MaSV=? AND MaMon=?");
            ps.setFloat(1, Float.parseFloat(txtDiem.getText()));
            ps.setString(2, cbSV.getSelectedItem().toString());
            ps.setString(3, cbMon.getSelectedItem().toString());
            ps.executeUpdate();
            conn.close();
            JOptionPane.showMessageDialog(this, "Cập nhật điểm thành công!");
            loadData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi sửa: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSuaActionPerformed

