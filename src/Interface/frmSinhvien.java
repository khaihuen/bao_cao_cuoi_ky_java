    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int r = table.getSelectedRow();
        if (r >= 0) {
            txtMa.setText(model.getValueAt(r, 0).toString());
            txtTen.setText(model.getValueAt(r, 1).toString());
            txtGT.setText(model.getValueAt(r, 2).toString());
            txtNS.setText(model.getValueAt(r, 3).toString());
            txtLop.setText(model.getValueAt(r, 4).toString());
            txtDTB.setText(model.getValueAt(r, 5).toString());
        }
    }//GEN-LAST:event_tableMouseClicked

