    private void btnXuatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXuatActionPerformed
        try {
            JFileChooser fc = new JFileChooser();
            if (fc.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
                File file = fc.getSelectedFile();
                WritableWorkbook wb = Workbook.createWorkbook(new File(file + ".xls"));
                WritableSheet sheet = wb.createSheet("SinhVien", 0);
                String[] cols = {"Mã SV", "Họ tên", "Giới tính", "Ngày sinh", "Mã lớp", "Điểm TB"};
                for (int i = 0; i < cols.length; i++) {
                    sheet.addCell(new Label(i, 0, cols[i]));
                }

                for (int i = 0; i < table.getRowCount(); i++) {
                    for (int j = 0; j < table.getColumnCount(); j++) {
                        sheet.addCell(new Label(j, i + 1, table.getValueAt(i, j).toString()));
                    }
                }

                wb.write();
                wb.close();
                JOptionPane.showMessageDialog(this, "Xuất file Excel thành công!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi xuất file: " + e.getMessage());
        }
    }//GEN-LAST:event_btnXuatActionPerformed

